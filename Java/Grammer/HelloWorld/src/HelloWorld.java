public class HelloWorld { // 클래스 블록
    public static void main(String[] args) { // 메서드 블록
        /*
        접근제어자 : public
        static : 메서드 -> 클래스메서드가 됨, 객체를 만들지 않아도 "클래스명.메서드명" 형태로 호출 가능
        void : 메서드의 리턴 타입 중 하나로 void는 리턴값이 없음을 의미
        String[] args : 메서드의 매게 변수, args 변수는 String[] 배열 자료형임을 의미
        */
        System.out.println("HelloWorld"); // 명령문 세미콜론 ;을 붙여 문장의 끝을 표시해야함
    }
}
